function calculate(num1, num2) {
    const result = num1 + num2;
    alert(`Oefening 1: ${num1} + ${num2} = ${result}`);
  }
  
  function multiplyByFive(num) {
    const result = num * 5;
    alert(`Oefening 2: ${num} * 5 = ${result}`);
  }
  
